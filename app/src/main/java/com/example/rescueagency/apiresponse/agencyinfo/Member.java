package com.example.rescueagency.apiresponse.agencyinfo;

public class Member {
    private int member_id;
    private String member_name;
    private int member_mobile;
    private String member_email;
    private String member_address;
    private String member_dob;
    private String member_role;
    private int member_experience;
    private String member_profile;

    public int getMember_id() {
        return member_id;
    }

    public String getMember_name() {
        return member_name;
    }

    public int getMember_mobile() {
        return member_mobile;
    }

    public String getMember_email() {
        return member_email;
    }

    public String getMember_address() {
        return member_address;
    }

    public String getMember_dob() {
        return member_dob;
    }

    public String getMember_role() {
        return member_role;
    }

    public int getMember_experience() {
        return member_experience;
    }

    public String getMember_profile() {
        return member_profile;
    }
}
